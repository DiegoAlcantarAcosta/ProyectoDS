package DTOs;

/**
 * Enumeración que representa diferentes tipos de bancos.
 * Estos son algunos ejemplos de bancos que podrían ser utilizados en un sistema.
 * 
 * Los valores de esta enumeración son:
 * - BANCOPPEL
 * - BBVA
 * - HSBC
 * - BANAMEX
 * - SANTANDER
 * - BANREGIO
 * 
 * Esta enumeración puede ser utilizada para identificar el tipo de banco en varios contextos dentro de un sistema.
 * Por ejemplo, al realizar operaciones financieras, al seleccionar opciones de pago, etc.
 * 
 * @author Wilber
 */
public enum tipoBancoDTO {
    
    BANCOPPEL,
    BBVA,
    HSBC,
    BANAMEX,
    SANTANDER,
    BANREGIO
}
