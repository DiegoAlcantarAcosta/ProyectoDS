    private void llenarTablaContactos(List<ContactoDTO> contactos) {
            DefaultTableModel model = (DefaultTableModel) tableContactos.getModel();
            model.setRowCount(0);

            for (ContactoDTO contacto : contactos) {
                Object[] row = {
                    contacto.getAlias()
                };
                model.addRow(row);
            
        }

    }

